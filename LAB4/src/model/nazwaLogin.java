package model;

public class nazwaLogin {

	private String nazwa;
	private String login;
	public nazwaLogin(String nazwa, String login) {
		super();
		this.nazwa = nazwa;
		this.login = login;
	}
	public String getNazwa() {
		return nazwa;
	}
	public void setNazwa(String nazwa) {
		this.nazwa = nazwa;
	}
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	
	
	
	
	
	
	
}
